#include<unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	x;
	int	b;
	int	s;

	x = 0;
	s = size - 1;
	while (x < (size / 2))
	{
		b = tab[x];
		tab[x] = tab[s];
		tab[s] = b;
		s--;
		x++;
	}
}
